                .append(worldTypeName(world))
                .append(".Guest) {\n")
            out.append("    this.").append(memberName(world.name())).append(" = guest\n")
            out.append("  }\n")
        }
        out.append("}\n\n")
        appendGuestExportRuntime(out, hasAsyncExport)
